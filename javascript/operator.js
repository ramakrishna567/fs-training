//unary operator
//prefix increase value and returns old value
var x = 10;
var z = x++;
console.log(z); //10
console.log(x); //11

//prefix increase value and returns new value
z=++x;
console.log(z); //12
console.log(x); //12

//prefix increase value and returns old value
var x = 10;
var z = x--;
console.log(z); //10
console.log(x); //11

//prefix increase value and returns new value
z=--x;
console.log(z); //12
console.log(x); //12
